function NavLink({
  href,
  label,
  icon,
  active,
}: {
  href: string;
  label: string;
  icon: string;
  active: boolean;
}) {
  return (
    <Link
      href={href}
      className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
        active
          ? "bg-slate-200 text-slate-900 dark:bg-slate-700 dark:text-slate-100"
          : "text-slate-600 hover:bg-slate-100 hover:text-slate-900 dark:text-slate-400 dark:hover:bg-slate-800 dark:hover:text-slate-100"
      }`}
    >
      <span className="shrink-0 text-slate-500 dark:text-slate-400" aria-hidden>
        <NavIcon name={icon} />
      </span>
      {label}
    </Link>
  );
}

export function Sidebar() {
  const pathname = usePathname();
  const isActive = (href: string) => (href === "/" ? pathname === "/" : pathname.startsWith(href));

  return (
    <aside
      className="flex w-56 shrink-0 flex-col border-r border-slate-200 bg-slate-50 dark:border-slate-800 dark:bg-slate-900/50"
      aria-label="Main navigation"
    >
      {/* User block */}
      <div className="flex items-center gap-3 border-b border-slate-200 px-4 py-5 dark:border-slate-800">
        <div
          className="flex h-10 w-10 shrink-0 items-center justify-center rounded-full bg-slate-300 text-sm font-semibold text-slate-600 dark:bg-slate-600 dark:text-slate-200"
          aria-hidden
        >
          JD
        </div>
        <span className="truncate text-sm font-medium text-slate-800 dark:text-slate-200">
          John Doe
        </span>
      </div>

      {/* Main nav */}
      <nav className="flex flex-1 flex-col gap-1 p-3" aria-label="Primary">
        {navItems.map((item) => (
          <NavLink
            key={item.href}
            href={item.href}
            label={item.label}
            icon={item.icon}
            active={isActive(item.href)}
          />
        ))}

        <div className="my-2 border-t border-slate-200 dark:border-slate-800" role="separator" />

        {bottomItems.map((item) => (
          <NavLink
            key={item.href}
            href={item.href}
            label={item.label}
            icon={item.icon}
            active={isActive(item.href)}
          />
        ))}
      </nav>
    </aside>
  );
}

